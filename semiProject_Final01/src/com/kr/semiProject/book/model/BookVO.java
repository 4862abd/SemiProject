package com.kr.semiProject.book.model;

public class BookVO {
	
	private String bookDate;
	private String bookTime;
	private String drName;
	private String bookItem;
	private String userId;
	
	public String getBookDate() {		return bookDate;	}
	public void setBookDate(String bookDate) {		this.bookDate = bookDate;	}
	public String getBookTime() {		return bookTime;	}
	public void setBookTime(String bookTime) {		this.bookTime = bookTime;	}
	public String getDrName() {		return drName;	}
	public void setDrName(String drName) {		this.drName = drName;	}
	public String getBookItem() {		return bookItem;	}
	public void setBookItem(String bookItem) {		this.bookItem = bookItem;	}
	public String getUserId() {		return userId;	}
	public void setUserId(String userId) {		this.userId = userId;	}
	
}
